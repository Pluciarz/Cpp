#include <iostream>
#include <cstdlib>
#include <time.h>
using namespace std;

int main()
{
    srand(time(NULL));
    int l, n=10;
    while (n!=0)
    {
        l = rand() % 6 + 1;
        cout << "Liczba wylosowana na kostce to: " << l << endl;
        if (l == 6)
        {
            cout << "Wylosowano 6 za " << 10 - n + 1 << " razem";
            break;
        }
        n--;
    }
}

